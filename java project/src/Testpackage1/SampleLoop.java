package Testpackage1;

public class SampleLoop {
	public static void main(String[] args) 
	{
		for(int a=5; a>=1; a--) 
		{
			for(int j=1; j<=a; j++) 
			{
				System.out.print("*");
			}
			System.out.println();
		}
			for(int b=1; b<=5; b++) 
			{
		        for(int i=1; i<=b; i++)
		        {
		        	System.out.print("*");
		        }
		        System.out.println();
	}

	}
}