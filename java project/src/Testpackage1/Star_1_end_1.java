package Testpackage1;

public class Star_1_end_1 {
	public static void main(String[] args)
	{
		for(int a=1; a<=5; a++) 
		{
			for(int j=1; j<=a; j++)
			{
				System.out.print("*");
			}
			System.out.println();
		}
		for(int a=4; a>=1; a--)
		{
			for(int j=1; j<=a; j++) 
			{
				System.out.print("*");
			}
			System.out.println();
		}
	}

}
