package Testpackage1;

public class incrementoperation1 {
	public static void main(String[] args) {
		int a = 2;
		System.out.println(a++);//post increment
		System.out.println(a);
		System.out.println(++a);//pre increment
		
	}

}  // increment operation ++
 
//preincrement:- ++a immediatly value +1 out put 3
//postincrement:- a++ next step value +1 out put 2
 //                                        out put 3
